
public enum GameState
{
    GAMESTART,TRANSITION,MINIGAME,MINIGAMEOVER,GAMEOVER
        /*GAMESTART-the start of the game
         * TRANSITION- transition to the next minigame
         * MINIGAME-Currently a minigame being played
         * MINIGAMEOVER- showing win/loss from previous minigame
         * GAMEOVER-all lives lost and game is over
         */
}
